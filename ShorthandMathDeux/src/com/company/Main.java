package com.company;

public class Main
{
    public static void main(String[] args)
    {
        int y = 12;

        y += 8;
        System.out.println("y is: " + y);

        y -= 5;
        System.out.println("y is: " + y);

        y *= 4;
        System.out.println("y is: " + y);

        y /= 2;
        System.out.println("y is: " + y);
    }
}
