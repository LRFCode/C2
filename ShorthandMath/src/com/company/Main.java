package com.company;

public class Main
{
    public static void main(String[] args)
    {
        int x = 1;

        x += 14; //x is assigned 15
        System.out.println("x is: " + x);

        x -= 5; //x is assigned 10
        System.out.println("x is: " + x);

        x *= 2; //x is assigned 20
        System.out.println("x is: " + x);

        x /= 4; //x is assigned 5
        System.out.println("x is: " + x);
    }
}
